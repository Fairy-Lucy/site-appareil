
CREATE TABLE appareils_photo (
    id SERIAL PRIMARY KEY,
    nom_appareil VARCHAR(255),
    pays VARCHAR(100),
    annee_debut INT,
    annee_fin INT,
    remarques VARCHAR(255),
    date_ajout DATE,
    description_id INT REFERENCES descriptions(id)
);
CREATE TABLE images_appareil (
    id SERIAL PRIMARY KEY,
    appareil_id INT REFERENCES appareils_photo(id),
    chemin VARCHAR(255) NULL
);
CREATE TABLE descriptions (
    id SERIAL PRIMARY KEY,
    modele VARCHAR(255),
    contenu TEXT NULL
);


